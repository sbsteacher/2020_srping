package com.kita.first;

public class Operator3 {
	public static void main(String[] args) {
		int n1 = 10;
		int cost = 3;
		n1 = n1 + cost;
		System.out.println("n1 : " + n1);
		
		int n2 = 10;
		n2 = n2 + 2;
		System.out.println("n2 : " + n2);
		
		int n3 = 10;
		n3 -= (-2);
		System.out.println("n3 : " + n3);
	}
}


